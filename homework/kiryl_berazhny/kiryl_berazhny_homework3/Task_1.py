# получение суммы, разности и произведения двух произвольных целых чисел

a = int(input('Введите целое число a: '))
b = int(input('Введите целое число b: '))
print('Сумма чисел a и b составляет:', a + b)
print('Разность чисел a и b составляет:', a - b)
print('Произведение чисел a и b составляет:', a * b)
